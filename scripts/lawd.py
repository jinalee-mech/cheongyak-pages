def resolve(addr: str):
    """주소 → (lawd_codes: list[str], dong: str). 못 찾으면 ([], '')."""
    if not addr:
        return [], ""
    # 괄호 안 실주소가 더 정확하면 사용 (예: '...검단신도시 AB23BL(인천광역시 서구 마전동 ...)')
    m = re.search(r"\(([^)]*(?:특별시|광역시|특별자치시|도)[^)]*)\)", addr)
    s = m.group(1).strip() if m else addr.strip()
    toks = s.split()
    if len(toks) < 2:
        return [], ""
    sido = _SIDO.get(toks[0])
    if not sido:
        return [], ""
    # 동: '동/리/가'로 끝나는 첫 토큰
    dong = next((t for t in toks[1:] if re.search(r"(동|리|가)$", t) and not t.endswith(("구", "시", "군"))), "")
    # 구 보유 시 우선
    si = next((t for t in toks[1:] if t.endswith("시")), "")
    gu = next((t for t in toks[1:] if t.endswith("구")), "")
    if si:
        ck = f"{sido} {si}"
        if ck in CITY_DISTRICTS:
            d = CITY_DISTRICTS[ck]
            if gu and gu in d:
                return [d[gu]], dong
            return list(dict.fromkeys(d.values())), dong   # 구 미상 → 전체 합산
    # 단순 시군구: 시/군/구 중 하나
    for t in toks[1:]:
        if t.endswith(("시", "군", "구")):
            code = SGG.get(f"{sido} {t}")
            if code:
                return [code], dong
    return [], dong
